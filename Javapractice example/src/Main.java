
import java.util.*;

public class Main {
	
	public static void printFactorial(int n) {
		
		int factorial=1;
		for(int i=n;1>=1;i--) {
			factorial=factorial*i;
		}
	}

public static void main(String[] args) {
	Scanner sc=new Scanner(System.in);
	int n=sc.nextInt();
	printFactorial(n);
	
	
	
			
	
	
	
	
	

	}
	
 }

